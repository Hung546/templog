static esp_err_t oled_send_cmd(i2c_port_t i2c_num, uint8_t cmd) {
  i2c_cmd_handle_t cmd_handler = i2c_cmd_link_create();
  i2c_master_start(cmd_handler);
  i2c_master_write_byte(cmd_handler, (OLED_ADD << 1) | I2C_MASTER_WRITE, true);
  i2c_master_write_byte(cmd_handler, 0x00, true);
  i2c_master_write_byte(cmd_handler, cmd, true);
  i2c_master_stop(cmd_handler);
  esp_err_t err =
      i2c_master_cmd_begin(i2c_num, cmd_handler, pdMS_TO_TICKS(1000));
  i2c_cmd_link_delete(cmd_handler);
  return err;
}

static esp_err_t oled_send_data(i2c_port_t i2c_num, uint8_t data) {
  i2c_cmd_handle_t cmd_handler = i2c_cmd_link_create();
  i2c_master_start(cmd_handler);
  i2c_master_write_byte(cmd_handler, (OLED_ADD << 1) | I2C_MASTER_WRITE, true);
  i2c_master_write_byte(cmd_handler, 0x40, true);
  i2c_master_write_byte(cmd_handler, data, true);
  i2c_master_stop(cmd_handler);
  esp_err_t err =
      i2c_master_cmd_begin(i2c_num, cmd_handler, pdMS_TO_TICKS(1000));
  i2c_cmd_link_delete(cmd_handler);
  return err;
}

void oled_init(i2c_port_t i2c_num) {
  uint8_t cmds[] = {0xAE, 0x20, 0x00, 0x21, 0x00, 0x7F, 0x22, 0x00,
                    0x07, 0x8D, 0x14, 0x81, 0xCF, 0xA1, 0xC8, 0xAF};

  for (int i = 0; i < sizeof(cmds); i++) {
    oled_send_cmd(i2c_num, cmds[i]);
  }
}

void oled_clear(i2c_port_t i2c_num) {
  for (uint8_t page = 0; page < 8; page++) {
    oled_send_cmd(i2c_num, 0xB0 + page);
    oled_send_cmd(i2c_num, 0x00);
    oled_send_cmd(i2c_num, 0x10);
    for (uint8_t col = 0; col < 128; col++) {
      oled_send_data(i2c_num, 0x00);
    }
  }
}

void oled_draw_bitmap(i2c_port_t i2c_num, uint8_t x, uint8_t y_page, uint8_t w,
                      uint8_t h, const uint8_t *bitmap) {
  uint8_t pages = h / 8;

  for (uint8_t p = 0; p < pages; p++) {
    oled_send_cmd(i2c_num, 0xB0 + y_page + p);
    oled_send_cmd(i2c_num, x & 0x0F);
    oled_send_cmd(i2c_num, 0x10 | (x >> 4));

    for (uint8_t col = 0; col < w; col++) {
      oled_send_data(i2c_num, bitmap[p * w + col]);
    }
  }
}

// ==============================================================================
// HAM IN CHUOI SO KHONG LO (Chuan 32px cao)
// ==============================================================================
void oled_draw_big_numbers(i2c_port_t i2c_num, uint8_t x, uint8_t y_page,
                           const char *str) {
  uint8_t current_x = x;

  while (*str) {
    char c = *str;

    if (c >= '0' && c <= '9') {
      uint8_t num = c - '0';
      oled_draw_bitmap(i2c_num, current_x, y_page, 16, 32, big_num_array[num]);
      // SUA KHOANG CACH CHU: Giam 18 xuong 17 (16px chu + 1px khoang trang)
      current_x += 17;

    } else if (c == '.') {
      oled_draw_bitmap(i2c_num, current_x, y_page, 8, 32, font_big_dot);
      // SUA KHOANG CACH DAU CHAM: Giam 10 xuong 9 (8px cham + 1px khoang trang)
      current_x += 9;

    } else if (c == ' ') {
      // Đong bo kich thuoc vung tay xoa de phu hop voi chu
      for (uint8_t p = 0; p < 4; p++) {
        oled_send_cmd(i2c_num, 0xB0 + y_page + p);
        oled_send_cmd(i2c_num, current_x & 0x0F);
        oled_send_cmd(i2c_num, 0x10 | (current_x >> 4));

        // Vong lap nay cung phai giam tu 18 xuong 17
        for (uint8_t col = 0; col < 17; col++) {
          if (current_x + col >= 96)
            break; // Phanh an toan bao ve icon
          oled_send_data(i2c_num, 0x00);
        }
      }
      // Tang con tro cho lenh xoa
      current_x += 17;
    }
    str++;
  }
}

