// Ham ve 1 ky tu ASCII
void oled_draw_char(i2c_port_t i2c_num, uint8_t x, uint8_t y_page, char c) {
  if (c < 32 || c > 127)
    return; // Bo qua cac ky tu khong xac dinh

  uint8_t font_index =
      c - 32; // Tru di 32 de khop voi vi tri trong mang font5x7

  // Set toa do
  oled_send_cmd(i2c_num, 0xB0 + y_page);
  oled_send_cmd(i2c_num, x & 0x0F);
  oled_send_cmd(i2c_num, 0x10 | (x >> 4));

  // Ve 5 cot du lieu cua ky tu
  for (uint8_t i = 0; i < 5; i++) {
    oled_send_data(i2c_num, font5x7[font_index][i]);
  }
  // Ve 1 cot khoang trang (Space) de cach chu tiep theo
  oled_send_data(i2c_num, 0x00);
}

// Ham ve ca mot chuoi (String)
void oled_draw_string(i2c_port_t i2c_num, uint8_t x, uint8_t y_page,
                      const char *str) {
  uint8_t current_x = x;
  while (*str) {
    oled_draw_char(i2c_num, current_x, y_page, *str);
    current_x += 6; // 5px chu + 1px khoang trang
    if (current_x > 122)
      break; // Chong tran man hinh
    str++;
  }
}

// Ham ve chu so co trung (16 rong x 24 cao)
void oled_draw_medium_numbers(i2c_port_t i2c_num, uint8_t x, uint8_t y_page,
                              const char *str) {
  uint8_t current_x = x;

  while (*str) {
    char c = *str;

    if (c >= '0' && c <= '9') {
      uint8_t num = c - '0';
      // Ve 16 cot ngang, cao 3 Page (24 pixel)
      oled_draw_bitmap(i2c_num, current_x, y_page, 16, 24,
                       medium_num_array[num]);
      current_x += 16;

    } else if (c == '.') {
      // Dau cham chi can rong 8 pixel la dep
      oled_draw_bitmap(i2c_num, current_x, y_page, 8, 24, font_medium_dot);
      current_x += 8;

    } else if (c == ' ') {
      // Tay xoa khoang trang chieu cao 3 Page
      for (uint8_t p = 0; p < 3; p++) {
        oled_send_cmd(i2c_num, 0xB0 + y_page + p);
        oled_send_cmd(i2c_num, current_x & 0x0F);
        oled_send_cmd(i2c_num, 0x10 | (current_x >> 4));
        for (uint8_t col = 0; col < 16; col++) {
          oled_send_data(i2c_num, 0x00);
        }
      }
      current_x += 16;
    }
    str++;
  }
}
